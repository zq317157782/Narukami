//读取二进制文件
inline std::vector<uint8_t> read_file_binary(const char* path)
{
    std::ifstream file(path, std::ios::binary);
    std::vector<uint8_t> file_byte_buffer;

    if (file.is_open())
    {
        file.seekg(0, std::ios::end);
        size_t size_bytes = file.tellg();
        file.seekg(0, std::ios::beg);
        file_byte_buffer.resize(size_bytes);
        if (file.read((char*)file_byte_buffer.data(), size_bytes)) return file_byte_buffer;
    }
    //else throw std::runtime_error("could not open binary ifstream to path " + path);
    return file_byte_buffer;
}

struct memory_buffer : public std::streambuf
{
    char * p_start {nullptr};
    char * p_end {nullptr};
    size_t size;

    memory_buffer(char const * first_elem, size_t size)
        : p_start(const_cast<char*>(first_elem)), p_end(p_start + size), size(size)
    {
        setg(p_start, p_start, p_end);
    }

    pos_type seekoff(off_type off, std::ios_base::seekdir dir, std::ios_base::openmode which) override
    {
        if (dir == std::ios_base::cur) gbump(static_cast<int>(off));
        else setg(p_start, (dir == std::ios_base::beg ? p_start : p_end) + off, p_end);
        return gptr() - p_start;
    }

    pos_type seekpos(pos_type pos, std::ios_base::openmode which) override
    {
        return seekoff(pos, std::ios_base::beg, which);
    }
};

struct memory_stream : virtual memory_buffer, public std::istream
{
    memory_stream(char const * first_elem, size_t size)
        : memory_buffer(first_elem, size), std::istream(static_cast<std::streambuf*>(this)) {}
};
